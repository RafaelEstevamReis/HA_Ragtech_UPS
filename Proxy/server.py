def enviar_e_receber(ip, porta, payload, dados_sensores):
    # Cria um socket TCP/IP
    cliente_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

    # Conecta ao servidor
    endereco_servidor = (ip, porta)
    cliente_socket.connect(endereco_servidor)

    try:
        payload_bytes = bytes(payload)
        cliente_socket.sendall(payload_bytes)

        # Recebe a resposta do servidor
        resposta = cliente_socket.recv(4096)
        resposta = resposta[:128]

        dados_sensores.update(resposta)
    except Exception as e:
        print(f"Erro ao obter dados: {e}")
        dados_sensores.is_success = False
    finally:
        # Fecha o socket
        cliente_socket.close()

def cutBytes(data, offset):
    bytes_extraidos = [
        data[offset + 2] ^ 0xa5,
        data[offset + 3] ^ 0xa5,
        data[offset] ^ 0xa5,
        data[offset + 1] ^ 0xa5
    ]
    return bytes(bytes_extraidos)

def getFloat(data, offset):
    bytes = cutBytes(data, offset)
    return round(struct.unpack('f', bytes)[0],1)

class DadosSensores:
    def __init__(self):
        self.vIn = None
        self.vOut = None
        self.wOut = None
        self.iOut = None
        self.pPot = None
        self.Freq = None
        self.vBat = None
        self.pBat = None
        self.Temp = None
        self.ultima_atualizacao = None
        self.is_success = False

    def update(self, resposta):
        try:
            self.vIn =  getFloat(resposta, 0x12)
            self.vOut = getFloat(resposta, 0x16)
            self.iOut = getFloat(resposta, 0x1A)
            self.wOut = round(self.vOut*self.iOut,1)

            self.pPot = getFloat(resposta, 0x1E)
            self.Freq = getFloat(resposta, 0x22)
            self.vBat = getFloat(resposta, 0x26)
            self.pBat = getFloat(resposta, 0x2E)
            self.Temp = getFloat(resposta, 0x2A)
            self.ultima_atualizacao = datetime.now()
            self.is_success = True

            data_hora = self.ultima_atualizacao.strftime("%Y-%m-%d %H:%M:%S")
            dados_formatados = (f"[{data_hora}] "
                                f"In: {self.vIn:.1f}v | "
                                f"Out: {self.vOut:.1f}v {self.iOut:.1f}A {self.wOut:.1f}W | "
                                f"Pot: {self.pPot:.1f}% | "
                                f"Freq: {self.Freq:.1f}Hz | "
                                f"Bat: {self.vBat:.1f}v {self.pBat:.1f}% | "
                                f"Temp: {self.Temp:.1f}C")
            print(dados_formatados)

        except Exception as e:
            print(f"Erro ao atualizar dados: {e}")
            self.is_success = False

